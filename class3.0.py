# A script using string methods...Chatting With Grandma
string = 'hello grandma, how are you?'
# capitalize method
stringcap = string.capitalize()
# uppercase method
stringup = string.upper()
# print an imaginary chat with grandma (she's hard of hearing)
print(string)
print('Whats that? I cant hear you, child.')
print(stringcap)
print('Youll have to speak up! My hearing isnt what it used to be.')
print(stringup)
print('Well you didnt need to shout, dear!')
